/*****************************************************************************
 * Function:    checkIspReg 
 * Description: This function compares the register values on ISP with the values
 *              provided by ST
 * Parameters:  instance
 * Return:      void
 *
 *****************************************************************************/
void DrvFwkCamIspTest(void)
{
    int i;
    for(i = 0; i < sizeof(mapArray)/sizeof(RegMap); i++)
    {
        DWORD value = 0;
        
        PRINTCOPY("Register: %s", mapArray[i].name);      

        if((mapArray[i].name[0] == 'u' && mapArray[i].name[1] == 'w') || 
                 mapArray[i].name[0] == 'w' || 
                 (mapArray[i].name[0] == 'f' && mapArray[i].name[1] == 'p'))
        {
            value = (DWORD)(CamSensorReadReg(mapArray[i].addr + 1) | (DWORD)(CamSensorReadReg(mapArray[i].addr) << 8));            
        }
        else
            value = (DWORD)CamSensorReadReg(mapArray[i].addr);
        
        if(mapArray[i].value == value)
            PRINT3N("   Addr(0x%04x) Value(ST: 0x%08x; RIM: 0x%08x)", mapArray[i].addr, mapArray[i].value, value);        
        else
            WARN3N("***Addr(0x%04x) Value(ST: 0x%08x; RIM: 0x%08x *** <-)", mapArray[i].addr, mapArray[i].value, value);        
    }    
}
